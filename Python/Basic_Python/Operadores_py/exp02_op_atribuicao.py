# 2. Operadores de Atribuição: Inicializando variáveis
a = 2
b = 5

# 2.1 o valor 10 é ATRIBUIDO a variável w:
w = 10
print(w)

# 2.2 Incrementação - Atribuimos + 3 unidades a variável a:
print(a)

a+=3
print(a)

# 2.3 Decrementação - Atribuimos -2 unidades a variavel b:
print(b)

b-=2
print(b)

# 2.4 Reinicializamos a variavel b atribuindo a ela o valor 5:
b = 5

# 2.5 As operações de atribuições sucessivas podem ocorrer com os demais operadores aritiméticos, veja:
b%=3
print(b)

b*=2
print(b)

a//=3
print(a)